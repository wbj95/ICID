package com.example.demo.model;

public class AssetManagement {
    private int idassetManagement;
    private String assetNumbles;
    private String assetTypes;
    private String assetClasses;
    private String userName;
    private String inTime;
    private String department;

    public int getIdassetManagement() {
        return idassetManagement;
    }

    public void setIdassetManagement(int idassetManagement) {
        this.idassetManagement = idassetManagement;
    }

    public String getAssetNumbles() {
        return assetNumbles;
    }

    public void setAssetNumbles(String assetNumbles) {
        this.assetNumbles = assetNumbles;
    }

    public String getAssetTypes() {
        return assetTypes;
    }

    public void setAssetTypes(String assetTypes) {
        this.assetTypes = assetTypes;
    }

    public String getAssetClasses() {
        return assetClasses;
    }

    public void setAssetClasses(String assetClasses) {
        this.assetClasses = assetClasses;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getInTime() {
        return inTime;
    }

    public void setInTime(String inTime) {
        this.inTime = inTime;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }
}
